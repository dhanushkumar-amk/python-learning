# Continue  => it is used to skip the particular iteration and goes to next iteration

for i in range(1,11):
    if(i % 2== 0):
        continue
    else:
        print(i)


# break statement is used to break the statement

for i in range(0,10):
    if(i==7):
        break
    else:
        print(i)
